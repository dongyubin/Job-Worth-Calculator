export const LanguageProvider: React.FC<{children: ReactNode; locale?: string}> = ({ children, locale }) => {
  // ä»propsæˆ–æœ¬åœ°å­˜å‚¨åˆå§‹åŒ–è¯­è¨€ï¼Œé»˜è®¤ä¸ºä¸­æ–‡
  const [language, setLanguageState] = useState<Language>(() => {
    if (locale && (locale === 'zh' || locale === 'en' || locale === 'ja')) {
      return locale as Language;
    }
    return 'zh';
  });

  // é¦–æ¬¡æ¸²æŸ“æ—¶æ£€æŸ¥æœ¬åœ°å­˜å‚¨çš„è¯­è¨€è®¾ç½®ï¼Œä½†ä¼˜å…ˆä½¿ç”¨propsä¸­çš„locale
  useEffect(() => {
    if (locale && (locale === 'zh' || locale === 'en' || locale === 'ja')) {
      setLanguageState(locale as Language);
    } else {
      const savedLanguage = localStorage.getItem('language') as Language;
      if (savedLanguage && (savedLanguage === 'zh' || savedLanguage === 'en' || savedLanguage === 'ja')) {
        setLanguageState(savedLanguage);
      }
    }
  }, [locale]);

  // è®¾ç½®è¯­è¨€å¹¶ä¿å­˜åˆ°æœ¬åœ°å­˜å‚¨
  const setLanguage = (newLanguage: Language) => {
    setLanguageState(newLanguage);
    localStorage.setItem('language', newLanguage);
  };

  // ç¿»è¯‘å‡½æ•°
  const t = (key: string): string => {
    return translations[language][key] || key;
  };

  return (
    <LanguageContext.Provider value={{ language, setLanguage, t }}>
      {children}
    </LanguageContext.Provider>
  );
};

